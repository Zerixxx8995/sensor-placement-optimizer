/**
 * Map a scalar value in [0, 1] to a viridis hex color string.
 * Values outside [0,1] are clamped.
 *
 * @param {number} value - Coverage probability in [0.0, 1.0]
 * @returns {string} CSS hex color, e.g. '#440154'
 */
export function valueToViridis(value) {
  const clamped = Math.max(0, Math.min(1, value));
  const index = Math.round(clamped * (VIRIDIS.length - 1));
  const [r, g, b] = VIRIDIS[index];
  return `#${r.toString(16).padStart(2, '0')}${g.toString(16).padStart(2, '0')}${b.toString(16).padStart(2, '0')}`;
}

/**
 * Same as valueToViridis but returns [r,g,b] array.
 * Useful for ImageData pixel-level rendering.
 *
 * @param {number} value
 * @returns {[number, number, number]}
 */
export function valueToViridisRGB(value) {
  const clamped = Math.max(0, Math.min(1, value));
  const index = Math.round(clamped * (VIRIDIS.length - 1));
  return VIRIDIS[index];
}
